    public static void DrawText(SpriteBatch spriteBatch, Texture2D pixel, string text, Vector2 position, Color color, int scale = 2)
    {
        var cursorX = (int)position.X;
        var cursorY = (int)position.Y;

        foreach (var rawChar in text)
        {
            if (rawChar == '\n')
            {
                cursorX = (int)position.X;
                cursorY += (GlyphHeight + 2) * scale;
                continue;
            }

            var c = char.ToUpperInvariant(rawChar);
            if (!Glyphs.TryGetValue(c, out var glyph))
            {
                c = ' ';
                glyph = Glyphs[c];
            }

            DrawGlyph(spriteBatch, pixel, glyph, cursorX, cursorY, color, scale);
            cursorX += (GlyphWidth + 1) * scale;
        }
    }

    private static void DrawGlyph(SpriteBatch spriteBatch, Texture2D pixel, IReadOnlyList<string> glyph, int x, int y, Color color, int scale)
    {
        for (var row = 0; row < glyph.Count; row++)
        {
            for (var column = 0; column < glyph[row].Length; column++)
            {
                if (glyph[row][column] != '1')
                {
                    continue;
                }

                var pixelRect = new Rectangle(x + (column * scale), y + (row * scale), scale, scale);
                spriteBatch.Draw(pixel, pixelRect, color);
            }
        }
    }
